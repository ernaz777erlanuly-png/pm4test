const OPTION_KEYS: OptionKey[] = ["A", "B", "C", "D", "E"];

const normalizeOptionText = (value: string) => value.replace(/\+/g, "").trim();

export const parseQuestions = (raw: string): Question[] => {
  const chunks = raw
    .split(/\n(?=\d+\)Сұрақ\.)/)
    .map((item) => item.trim())
    .filter(Boolean);

  return chunks
    .map((chunk) => {
      const lines = chunk.split("\n").map((line) => line.trim());
      const header = lines[0] ?? "";
      const headerMatch = header.match(/^(\d+)\)Сұрақ\.\s*(.+)$/);
      if (!headerMatch) {
        return null;
      }

      const id = Number(headerMatch[1]);
      const text = headerMatch[2].trim();
      const optionLines = lines.filter((line) => /^[A-E]\)/.test(line));

      const options = optionLines.map((line) => {
        const match = line.match(/^([A-E])\)\s*(.*)$/);
        const key = (match?.[1] ?? "A") as OptionKey;
        const rawText = match?.[2] ?? "";

        return {
          key,
          text: normalizeOptionText(rawText),
          isCorrect: rawText.includes("+"),
        };
      });

      const normalizedOptions = OPTION_KEYS.map((key) => {
        const found = options.find((option) => option.key === key);
        return found ?? { key, text: "", isCorrect: false };
      });

      let correctKey = normalizedOptions.find((option) => option.isCorrect)?.key;

      // Q35 in the source list is incomplete, so we restore the expected answer.
      if (id === 35) {
        normalizedOptions[0] = { key: "A", text: "Тестілеу", isCorrect: true };
        correctKey = "A";
      }

      if (!correctKey) {
        correctKey = "A";
      }

      return {
        id,
        text,
        options: normalizedOptions.map(({ key, text: optionText }) => ({ key, text: optionText })),
        correctKey,
      } as Question;
    })
    .filter((question): question is Question => Boolean(question))
    .sort((a, b) => a.id - b.id);
};

export const ALL_QUESTIONS = parseQuestions(RAW_QUESTIONS);